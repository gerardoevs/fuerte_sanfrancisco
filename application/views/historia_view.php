					La historia del Fuerte comenzó a escribirse en los años treinta bajo el nombre de “Atlahuanca”, que luego pasa a llamarse “Invencibles”, que tuvo la peculiaridad de tener jugando cuatro hermanos al mismo tiempo: Arnoldo, Atilio, Juan y Roberto todos de apellido Rodezno.<br><br>
					En los años cuarenta, cuando entre los primeros equipos de Gotera se contaban el Invencible y el Xelaju, eran los años cuando se jugaba por amor a los colores. La época romántica del fútbol local, rustico, talvez, pero que apasionaba a las multitudes por la entrega de cada integrante. Pues bien, en ese marco nace el Fuerte San Francisco, su nombre se da porque desde un inicio era un equipo de choque aparte de ser el único equipo que daba la cara por Gotera, según se dice fue Juan Villegas quien le puso ese nombre.<br><br>
					Los otros nombres con los que se conoció fueron Cuscatlán y Morazán, el nombre Fuerte San Francisco se le dio el 04 de octubre  de 1952 y la fecha de nacimiento por acuerdo oficial es el 08 de enero de 1953.<br><br>
					El Fuerte jugaba con equipos de El Divisadero, Jocoro, Gujatajiagua y la hacienda El Platanar. Los viajes para realizar estos partidos los hacían en caballo y los gastos los pagaban los mismos jugadores al igual que la compra de los implementos deportivos.<br><br>
					En 1954 el equipo participa por primera vez en la liga federada, el primer presidente del equipo ya legalmente establecido fue el señor Enrique Ferrufino y secretario el señor Fernando Antonio Llanes, a lo largo de la historia del Fuerte San Francisco se tiene registrado como hecho más importante el ascenso a primera división o liga mayor A el 11 de marzo de 1990.<br><br>
					Gloria pasada del club<br><br>
					EL equipo de San Francisco Gotera que a inicios de la década de los noventa marcó historia en el fútbol nacional en la Liga Mayor, aquellas tardes de gloria en el estadio Luis Amílcar Moreno con el plantel conformado, entre otros, por los uruguayos Rubén Alonso, Gustavo Faral y Raúl Esnal (Q.E.P.D.), Modesto Henríquez, Carlos Aragón, David Omar, el Tractor,  Sevilla, Osmel Zapata y el hondureño Jorge Martínez Ugalde (Q.E.P.D.).<br><br>
					Luego vino la generación de los hermanos Galdámez Callejas, el paraguayo Julio César Chávez, Alcides Caballero y su figura estelar Marlon Medrano, quien en su primer torneo de la primera división jugó con el número 30, mismo que traía desde la categoría de reservas.<br><br>
					Medrano tuvo un buen suceso en la liga mayor y luego saltó a FAS, Limeño y otros clubes. Eso también le sirvió al atacante para tener un paso en la Azul. Esos eran los tiempos de los   azules, el título con el que la afición de Gotera bautizó al Fuerte San Francisco. Había jugadores del plantel que aparecían con vinchas azules en la cabeza para combinar con el uniforme que era del mismo color.<br><br>
					El plantel de Gotera no pudo acceder a fase de semifinales en la temporada 1991-1992. Fue a un repechaje contra Águila y cayó 2-1 en el estadio Cuscatlán. Eso fue lo más lejos a lo que llegó durante su paso por el circuito de privilegio. En 1993, las cosas no le salieron bien y tuvieron que dejar la categoría.<br><br>
					Ricardo, la Coneja, Guardado inició como técnico y terminó Henry Arias. Era el retorno a la segunda división del fútbol nacional.<br><br>
					En 2004, bajo la conducción técnica de Omar Sevilla, Fuerte San Francisco estuvo a punto de regresar al circuito de privilegio. Hizo una buena temporada regular y llegó como líder a la fase de semifinales. Pero sucumbió contra Once Lobos y no logró el objetivo. Siguió en la liga de ascenso.<br><br>
					Sin embargo, después de ganar tanto el Apertura y Clausura 2014-2015 de la tercera División logran el Ascenso a Segunda división.<br><br>
					Juega en casa del extinto equipo  Correcaminos, Estadio con una capacidad de 12.000. Que solía ser la casa del Vista Hermosa, el Fuerte San Francisco  comparten el estadio con CD Chaguite. Previamente jugó en el Estadio Luis Amílcar Moreno en San Francisco Gotera.<br><br>
					El amor de la población goterense por este equipo sigue vigente, el apoyo de la afición siempre ha sido incondicional, a pesar de los resultados, sean positivos o negativos. Cada triunfo y cada derrota la viven a flor de piel los centenares de aficionados quienes desde diferentes sectores no escatiman sacrificios para acompañar a los integrantes del plantel, el apoyo también proviene de goterenses en el exterior, desde Estados Unidos un grupo de personas denominadas GOTUS, sustentan gastos del equipo, asimismo el Comité de apoyo del Fuerte San Francisco además de alcaldes, empresa privada y la barra azul inseparable motivadora del equipo.<br><br>

